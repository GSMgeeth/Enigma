package Role;

public class Category
{
    private int catId;
    private String catName;

    public void setCatName(String catName)
    {
        this.catName = catName;
    }

    public String getCatName()
    {
        return catName;
    }

    public int getCatId()
    {
        return catId;
    }

    public void setCatId(int catId)
    {
        this.catId = catId;
    }
}
